    //combine all the arrays of arrays of string to 1 array of string for rs
    private ArrayList<String> combine_array(ArrayList<ArrayList<String>> aal,ArrayList<String> cond, ArrayList<String> field) {
        ArrayList<String> rs = new ArrayList<String>();
        
        for (int i = 0; i < field.size(); i += 1) {
            
            
            if (cond.get(i).equals("AND")) {
                rs = this.take_dup(rs,aal.get(i));
                
            }
            else if (cond.get(i).equals("OR")) {
                rs = this.remove_dup(rs,aal.get(i));
                
            }
            else if (cond.get(i).equals("NONE") && !field.get(i).equals("")) {
                rs = this.remove_dup(rs,aal.get(i));
                
            }
            else {
                //do nothing
                
            }
        }
        return rs;
    }
    //remove duplicate of 2 array (OR)
    private ArrayList<String> remove_dup(ArrayList<String> a1, ArrayList<String> a2) {
        ArrayList<String> rs = new ArrayList<String>();
        ArrayList<String> dup = new ArrayList<String>();
        boolean b = true;
        if (a1.isEmpty() || a1 == null) {
            return a2;
        }
        if (a2.isEmpty() || a2 == null) {
            return a1;
        }
        for (int i = 0; i < a1.size(); i += 1) {
            b = true;
            for (int a = 0; a < a2.size(); a += 1) {
                if (a1.get(i).equals(a2.get(a))) {
                    b = false;
                }
            }
            if (b) {
                rs.add(a1.get(i));
            } else {
                dup.add(a1.get(i));
            }
        }
        for (int a = 0; a < a2.size(); a += 1) {
            b = true;
            for (int i = 0; i < dup.size(); i += 1) {
                if (a2.get(a).equals(dup.get(i))) {
                    b = false;
                }
                        
            }
            if (b) {
                rs.add(a2.get(a));
            }
        }
        
        return rs;
    }
    //take the dupllicates (AND)
    private ArrayList<String> take_dup(ArrayList<String> a1, ArrayList<String> a2) {
        ArrayList<String> dup = new ArrayList<String>();
        boolean b = true;
        if (a1.size() == 0 || a1 == null) {
            return a2;
        }
        if (a2.size() == 0 || a2 == null) {
            return a1;
        }
        for (int i = 0; i < a1.size(); i += 1) {
            b = true;
            for (int a = 0; a < a2.size(); a += 1) {
                if (a1.get(i).equals(a2.get(a))) {
                    b = false;
                }
            }
            if (!b) {
                dup.add(a1.get(i));
            }
        }
        return dup;
    }
